package Innerforloop;

public class innerforloopQ1star {
	
	void bhatu ()
	{
		for (int b =1; b<=4; b++)
		{
			for (int p=1;p<=b; p++)
			{
				
				System.out.print("*");
			}
			System.out.println();
			
		}
		
	}
	
	public static void main(String[] args) {
		 innerforloopQ1star obj = new  innerforloopQ1star();
		 obj.bhatu();	}

}
